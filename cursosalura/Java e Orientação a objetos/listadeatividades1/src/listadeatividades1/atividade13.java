package listadeatividades1;

public class atividade13 {
	public static void main(String args[]) {
		
		double salarioDoFuncionario = 1000;
		double porcetagemDeAumentoDoSalario = (salarioDoFuncionario * 15) / 100;
		double valorDoReajusteDoSalario = salarioDoFuncionario + porcetagemDeAumentoDoSalario;
		
		System.out.println("O salário do funcionário é de R$" + salarioDoFuncionario + " reais");
		System.out.println("O valor do reajuste de 15% do salário é de R$ " + valorDoReajusteDoSalario + " reais");
		
		
		
	}
}

